    public void analizadorLexico(String code) {
        Lexer lexer = new Lexer(new StringReader(code));
        String resultado = "";
        while (true) {
            try {
                Tokens tokens = lexer.yylex();
                if (tokens == null) {
                    resultado += "FIN";
                    //txtLexico.setText(resultado);
                    llenarTabla();
                    return;
                }

                switch (tokens) {
                    case ERROR:
                        resultado += "Simbolo no definido\n";
                        this.tokens.add(new Token("Simbolo no definido", "ERROR"));
                        break;
                    case Identificador:
                    case Numero:
                    case T_dato:
                    case P_coma:
                    case Coma:
                    case Punto:
                        this.tokens.add(new Token(lexer.lexeme, tokens.toString()));
                        resultado += lexer.lexeme + " : Es un " + tokens + "\n";
                        break;
                    default:
                        this.tokens.add(new Token("Token", tokens.toString()));
                        resultado += "Token : " + tokens + "\n";
                        break;

                }
            } catch (IOException ex) {
                Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void analizadorSintactico(String code) {
        Sintax sintax = new Sintax(new analizador.LexerCup(new StringReader(code)));

        try {
            sintax.parse();
            txtSintactico.setText("Analisis realizado correctamente...");
            txtSintactico.setForeground(new Color(25, 111, 61));
        } catch (Exception ex) {
            Symbol sym = sintax.getS();
            txtSintactico.setText("Error de sintaxis. Linea " + (sym.right + 1) + " Columna " + (sym.left + 1) + ", Texto: \"" + sym.value + "\"");
            txtSintactico.setForeground(Color.red);
        }
    }

    public void initTabla() {
        try {
            dtmTokens.addColumn("TIPO");
            dtmTokens.addColumn("TOKEN");
            tblLexico.setModel(dtmTokens);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al iniciar las tablas : " + ex);
        }
    }

    public void clearTabla() {
        int a = dtmTokens.getRowCount();
        for (int i = 0; i < a; i++) {
            dtmTokens.removeRow(0);
        }
        tokens.clear();
    }

    public void llenarTabla() {
        for (Token token : tokens) {
            dtmTokens.addRow(new Object[]{token.getTipo(), token.getValor()});
        }
    }

